package _3ArithmeticOperators;

public class ArithmeticOperatorsJava {
    public static void main(String[] args) {

        // Arithmetic Operators

        int x = 10, y = 2, add, subtract, divide, multiply, modulus;

        add = x + y; // addition is +
        subtract = x - y; // subtraction is -
        divide = x / y; // division is /
        multiply = x * y; // multiplication is *
        modulus = x % y; // modulus means remainder, basically when you divide and then, if it has a remainder, modulus will show it

        System.out.println("Addition arithmetic: " + add);
        System.out.println("Subtraction arithmetic: " + subtract);
        System.out.println("Division arithmetic: " + divide);
        System.out.println("Multiplication arithmetic: " + multiply);
        System.out.println("Modulus arithmetic: " + modulus);

    }
}
