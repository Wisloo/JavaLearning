package _3ArithmeticOperators;

public class IncrementAndDecrementOperators {
    public static void main(String[] args) {
        // Increment and Decrement Operators

        int x = 1;

        /* x++;
           x--;

         Increment and decrement operators are mostly used in looping structures. if we are only incrementing by 1
         then it is better to us ++ or if we are decrementing by 1 then better use --*/

        // x++;
        x--;

        System.out.print(x);

    }
}
